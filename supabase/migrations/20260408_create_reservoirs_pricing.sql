-- Create table for def/dif pricing reservoirs
CREATE TABLE IF NOT EXISTS public.reservoirs_pricing (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    code TEXT,
    designation TEXT,
    prix DECIMAL(10,2),
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- RLS policies
ALTER TABLE public.reservoirs_pricing ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Enable read access for all users" ON public.reservoirs_pricing FOR SELECT USING (true);
CREATE POLICY "Enable insert for all users" ON public.reservoirs_pricing FOR INSERT WITH CHECK (true);
CREATE POLICY "Enable update for all users" ON public.reservoirs_pricing FOR UPDATE USING (true);
CREATE POLICY "Enable delete for all users" ON public.reservoirs_pricing FOR DELETE USING (true);

